#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	ch[2];

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			ch[0] = a / 10 + '0';
			ch[1] = a % 10 + '0';
			write (1, ch, 2);
			write (1, " ", 1);
			ch[0] = b / 10 + '0';
			ch[1] = b % 10 + '0';
			write (1, ch, 2);
			if (a != 98 || b != 99)
				write (1, ", ", 2);
			b++;
		}
		a++;
	}
}
